from typing import List
from collections import defaultdict


# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next

class Solution:
    def leetcode(self, head: Optional[ListNode]) -> Optional[ListNode]:
        index0 = head
        index1 = index0.next
        while index1:
            if index1.val == 0:
                if index1.next == None:
                    index0.next = None
                    index1 = None
                    break
                else:
                    index0.next = index1
                    index0 = index1
            else:
                index0.val += index1.val
            index1 = index1.next

        return head

