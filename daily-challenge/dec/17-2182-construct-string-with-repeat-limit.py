class Solution:
    def leetcode(self, s: str, repeatLimit: int) -> str:
        ll = len(s)
        q = [0]*26
        za = "zyxwvutsrqponmlkjihgfedcba"
        for each in s:
            q[25-(ord(each)-ord("a"))] += 1
        
        first = 0
        second = 0
        result = ""
        while first < 26:
            if q[first] == 0:
                first += 1
            else:
                repeated = min(q[first],repeatLimit)
                result += za[first]*repeated
                q[first] -= repeated
                if q[first] == 0:
                    first += 1
                    continue

                second = first+1
                while second < 26 and q[second] == 0:
                    second += 1
                if second == 26:
                    return result
                result += za[second]
                q[second] -= 1

        return result


