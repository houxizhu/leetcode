class Solution:
    def leetcode(self, s: str) -> int:
        ll = len(s)
        result = 0
        window = 0
        start = 0
        for ii in range(ll):
            if s[ii] in s[start:ii]:
                result = max(result,ii-start)
                while s[start] != s[ii]:
                    start += 1
                start += 1
        result = max(result,ll-start)

        return result


